//Início função da lupa apenas onde o cursos passa
// **************************

$("#lupaTexto").click(function (e) {
  if ($("body").hasClass("LupaTexto")) {
    $("body").removeClass("LupaTexto");
  } else {
    $("body").addClass("LupaTexto");
  }
});
